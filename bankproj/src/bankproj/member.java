package bankproj;

public class member {
	private String name,email,pass;
	public member(String name, String email, String pass) {
	
		this.name = name;
		this.email = email;
		this.pass = pass;
		}

	public String getName() {
		return name;
	}	

	public String getEmail() {
		return email;
	}
	public String getPass() {
		return pass;
	}

	
	

}
